#include "main.h"
#include <stdio.h>
#include <ctype.h>
/**
 * _isalpha - Checks for lowercase character
 * @c: The character to be checked.
 * Return: 1 if character is lowercase , 0 otherwise.
 */
int isalpha(int c)
{

	int e;
	if (isalpha(c))
	{
		e = 1;
	}
	else
	{
		e = 0;
	
	}
	return (e);

}
